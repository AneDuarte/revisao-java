public class HelloWorld {
    public static void main (String[] args) {
        System.out.println("Hello World!");
    }
}

//class (linha 1) declaração da classe
//HelloWorld (linha 1) nome da classe
//(linha 2 a 4) método
//public static (linha 2) modificadores de acesso do método
//void (linha 2) tipo de retorno do método. Nesse caso void pois não retorna nada, apenas imprime um texto.
//main (linha 2) nome do método. Quando você faz sua própria classe pode dar o nome que quiser.
//String[] args (linha 2) parâmetros/argumentos do método
//(linha 3) código que desejamos executar
//blocos ficam entre {}
//Java é case sensitive!!!